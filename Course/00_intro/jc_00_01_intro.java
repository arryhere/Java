public class jc_00_01_intro {
  public static void main(String[] args) {
    System.out.print("Hello World ! ");
    System.out.println("Hello World, i am Arijit Das.");
  }
}

/*
 * compile : javac test.java
 * execute : java test
 * source-code -->(compier)--> byte-code -->(JVM)--> machine-code
 */

/*
 * class : keyword is used to declare a class in Java
 * public : keyword is an access modifier that represents visibility. It means
 * it is visible to all.
 * static : static method can be called with class reference , no object
 * creation is required to call static method
 * void : is the return type of the method. It means it doesn't return any
 * value.
 * main : represents the starting point of the program
 * String[] args or String args[] : is used for command line argument
 * System.out.println() : is used to print statement
 */